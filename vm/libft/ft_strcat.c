char	*ft_strcat(char *s1, const char *s2)
{
	char	*s2_iter;
	char	*s1_start;

	s1_start = s1;
	s2_iter = (char *)s2;
	while (*s1 != '\0')
		s1++;
	while (*s2_iter != '\0')
	{
		*s1 = *s2_iter;
		s1++;
		s2_iter++;
	}
	*s1 = '\0';
	return (s1_start);
}
